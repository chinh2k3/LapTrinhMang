    private void jButtonSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSelectActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            try {
                File file = fileChooser.getSelectedFile();
                // Lấy phần mở rộng của file jpg,png
                String fileName = file.getName();
                this.fileExtention = fileName.substring(fileName.lastIndexOf(".") + 1);

                // Read image
                this.img = ImageIO.read(file);
                //Output image
                jLabelImage.setIcon(new ImageIcon(img));
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonSelectActionPerformed
    //Phương thức chuyển hình ảnh thành mảng ký tự
    public static BufferedImage toBuffered(Image img) {
        if (img instanceof BufferedImage) {
            return (BufferedImage) img;
        }
        BufferedImage bufferedImage = new BufferedImage(img.getWidth(null), img.getHeight(null), BufferedImage.TYPE_INT_ARGB);
        bufferedImage.getGraphics().drawImage(img, 0, 0, null);
        return bufferedImage;
    }
    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String name = jTextFieldName.getText();
        String address = jTextFieldAddress.getText();
        int phoneNumber = Integer.parseInt(jTextFieldPhoneNumber.getText());
        String email = jTextFieldEmail.getText();
        String image = "";
        try {
            BufferedImage bufferedImage = toBuffered(img);
            ByteArrayOutputStream baos = new ByteArrayOutputStream(1000);
            ImageIO.write(bufferedImage, this.fileExtention, baos);
            byte[] imageByte = baos.toByteArray();
            image = Base64.getEncoder().encodeToString(imageByte);
        } catch (Exception ex) {
            ex.printStackTrace();
        }

        this.stuCard = new StudentCard(name, address, phoneNumber, email, image);
        // Lưu xuống file
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        try {
            int result = fileChooser.showOpenDialog(this);
            int saveResult = fileChooser.showSaveDialog(this);
            if(saveResult == JFileChooser.APPROVE_OPTION){
                File file = fileChooser.getSelectedFile();
                if( !file.exists()){
                    file.createNewFile();
                }
                //lưu đối tượng xuống file 
                FileOutputStream fos = new FileOutputStream(file);
                ObjectOutputStream oos= new ObjectOutputStream(fos);
                oos.writeObject(this.stuCard);
                oos.flush();
                oos.close();
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButtonSaveActionPerformed

