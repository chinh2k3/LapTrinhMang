    private static Image ConverImage(byte[] imageByte){
        try{
            ByteArrayInputStream bis = new ByteArrayInputStream(imageByte);
            BufferedImage buffer = ImageIO.read(bis);
            return buffer;
        }catch(Exception ex){
            return null;
        }
        
    }
    
    private void jButtonInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInputActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        try{
            int result = fileChooser.showOpenDialog(this);
            if(result == JFileChooser.APPROVE_OPTION){
                File file = fileChooser.getSelectedFile();
                FileInputStream fis = new FileInputStream(file);
                ObjectInputStream ois = new ObjectInputStream(fis);
                
                this.stuCard = (StudentCard) ois.readObject();
                showInfo();
                ois.close();
            }
        }catch(Exception ex){
            
        }
    }//GEN-LAST:event_jButtonInputActionPerformed
    public void showInfo(){
        this.jTextFieldName.setText(this.stuCard.getName());
        this.jTextFieldAddress.setText(this.stuCard.getAddress());
        this.jTextFieldPhoneNumber.setText(String.valueOf(this.stuCard.getPhonenumber()));
        this.jTextFieldEmail.setText(this.stuCard.getEmail());
        //Hiển thị hình ảnh ra màn hình
        try{
            byte[] imageByte = Base64.getDecoder().decode(this.stuCard.getImage());
             this.img = ConverImage(imageByte);
             this.jLabelImage.setIcon(new ImageIcon(this.img));
        }catch(Exception ex){
            
        }
    }
